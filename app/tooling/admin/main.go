	var parseClaims Claims
	parsedToken, err := parser.ParseWithClaims(tokenStr, &parseClaims, keyFunc)
	if err != nil {
		return fmt.Errorf("parsing token: %w", err)
	}

	if !parsedToken.Valid {
		return errors.New("invalid token")
	}

